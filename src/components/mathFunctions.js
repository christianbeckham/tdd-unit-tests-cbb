const add = (numberOne, numberTwo) => {
	return numberOne + numberTwo;
};

const divide = (numberOne, numberTwo) => {
	return numberOne / numberTwo;
};

const multiply = (numberOne, numberTwo) => {
	return numberOne * numberTwo;
};

const subtract = (numberOne, numberTwo) => {
	return numberOne - numberTwo;
};

export { add, divide, multiply, subtract };
